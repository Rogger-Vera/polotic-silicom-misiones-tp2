    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try{
            int num_cliente = Integer.parseInt(txtCliente.getText());
            String nombre_perro = txtNombre.getText();
            String raza = txtRaza.getText();
            String color = txtColor.getText();
            String alergico = (String) cmbAlergico.getSelectedItem();
            String atencion_especial = (String) cmbAtencion.getSelectedItem();;
            String nombre_duenio = txtNombreDueño.getText();
            String celular_duenio = txtCelDueño.getText();
            String observaciones = txtObservaciones.getText();
            
            
            controlador.crearMascota(num_cliente, nombre_perro, raza, color, alergico, atencion_especial, nombre_duenio, celular_duenio, observaciones);
            JOptionPane.showMessageDialog(rootPane, "Mascota agregada correctamente", "Mascota agregada", JOptionPane.INFORMATION_MESSAGE);
            
        }catch(NumberFormatException n){
            //capturamos excepcion cuando no se ingresa un campo numerico
            JOptionPane.showMessageDialog(rootPane, "El campo Cliente N° debe ser numerico", "Mascota NO agregada", JOptionPane.ERROR_MESSAGE);
        }catch(Exception e){
            //capturamos cualquier otro tipo de excepcion
            JOptionPane.showMessageDialog(rootPane, "No se pudo agregar la mascota\nVerifique los datos", "Mascota NO agregada", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

